            <h2 class="text-2xl font-medium">Vowel Length</h2>
            <p class="indent-4">
                Ancient Greek had a feature not present in Modern Greek known as
                phonemic vowel length. Vowels and dipthongs could be either long
                or short, and this was not always written. Long vowels would be
                pronounced for longer, probably somewhere between 1.3x to 2x the
                amount of time as a short vowel. Some words could be
                distinguished from another solely based on vowel length. An
                example of this phenomenon is ἵσταμεν vs ῑ̔́σταμεν, which differ
                in tense based solely on the length of the initial ι.
            </p>
            <p class="indent-4">
                Of the Greek letters and dipthongs, ω and η were always long, as
                were ει, ου, υι, αυ, and ευ. ε and ο were always short. α, ι, υ,
                αι, and οι could be long or short depending on the word. Some
                greek texts will distinguish long α, ι, and υ by writing a
                macron over top of them, like ᾱ, ῑ, ῡ. αι and οι are usually
                long, except in certain noun and verb endings. There is usually
                no marker for if they are long or short.
            </p>
            <p class="indent-4">
                Greek also at one point had what were called long dipthongs.
                These were ωι, ηι, ᾱι, ηυ, and ᾱυ. ωι, ηι, ᾱι would later come
                to be written with the ι underneath the main letter since it
                wasn't pronounced by that point. The long dipthongs would likely
                have had a long initial vowel followed by a glide.
            </p>
            <p>
                Greek also had so-called geminated consonants. Any time a ρ
                began a word, or two of the same letter were adjacent, the
                sounds were held longer. For ρ, this meant a using a trill
                instead of a tap. For stops, this meant pausing momentarily
                before releasing the stop. For fricatives this meant simply
                producing the fricative for longer.
            </p>
            <p class="indent-4">
                Not all the systems listed on this website use this feature.
                This feature had started to weaken in some dialects sometime in
                the 1st century A.D., and disappeared for most Greek speakers
                sometime between the middle of the 2nd century A.D. and the
                Byzantine era.
            </p>
